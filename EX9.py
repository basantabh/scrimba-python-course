#USING FOR LOOPS

for i in range(1,5):
    print(f"{i*"--"}{">"} Loops are really cool {i*"!"}")
# Three Loop Questions:
#1. What do I want to repeat?
#  -> message
#2. What do I want to change each time?
#  -> stars
#3. How long should we repeat?
#  -> 5 times

# USING WHILE LOOPS
i=1
while i<6:
    i+=1
    print(f"{i*"-"}{">"} Loops are really cool {i*"!"}")